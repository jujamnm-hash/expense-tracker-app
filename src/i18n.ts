export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations.ku;

export const getTranslation = (lang: Language, key: TranslationKey): string => {
  return translations[lang][key] || translations.ku[key];
};

export const getCurrencySymbol = (currency: 'IQD' | 'USD' | 'EUR'): string => {
  const symbols = {
    IQD: 'د.ع',
    USD: '$',
    EUR: '€',
  };
  return symbols[currency];
};

export const getCurrencyName = (currency: 'IQD' | 'USD' | 'EUR', lang: Language): string => {
  const names = {
    IQD: { ku: 'دینار', ar: 'دينار', en: 'Dinar' },
    USD: { ku: 'دۆلار', ar: 'دولار', en: 'Dollar' },
    EUR: { ku: 'یۆرۆ', ar: 'يورو', en: 'Euro' },
  };
  return names[currency][lang];
};
